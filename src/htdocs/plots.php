<h2>Parkfield</h2>

<p>Since the <a href="/research/parkfield/">Parkfield</a> area is a region of fairly frequent, moderate-sized earthquakes, the USGS installed a variety of instruments that measure strain changes and slip. On September 28, 2004 a Magnitude 6.0 earthquake occurred 11 kilometers southeast of the town of Parkfield. The USGS continues to monitor these instruments to document the deformation prior to, during, and after such events. The role that these earthquakes play in the eventual failure of the locked section of the San Andreas fault just to the south is also of great interest.</p>


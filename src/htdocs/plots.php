<h2>San Juan Bautista</h2>

<p>The San Juan Bautista area covers the junction between the northern end of the creeping section of the San Andreas Fault and the southern section of the (now locked) region of the fault that last ruptured during the great 1906 San Francisco earthquake. The region thus forms a natural laboratory where the processes of stable sliding (creep) and unstable sliding (earthquakes) is closely monitored. Moderate magnitude earthquakes have occurred quite frequently in this region in the past. The largest and most recent event was the 1989 M7.1 Loma Prieta earthquake.</p>


<h2>Long Valley</h2>

<p>Long Valley Caldera is located 20 km south of Mono Lake along the east side of the Sierra Nevada in east-central California. This area of eastern California has produced numerous volcanic eruptions over the past 3 million years including the massive, caldera-forming eruption 760,000 years ago. The most recent eruptions occurred just 500&ndash;600 years ago from vents along the Mono Crater-Inyo crater volcanic chain, which extends south from Mono Lake into the western part of Long Valley caldera.</p>

<p>A strong earthquake swarm in May of 1980, which included four magnitude 6 earthquakes near the southern margin of the caldera and increased uplift and extension of the Long Valley Caldera, marked the onset of the latest a period of activity in the caldera. This activity continues today and includes recurring earthquake swarms and continued dome-shaped uplift of the central section of the caldera accompanied by changes in thermal springs and gas emissions.</p>

<p>In addition to crustal deformation measurements, the USGS <a href="https://volcanoes.usgs.gov/observatories/calvo/"> California Volcano Observatory</a> monitors earthquakes, hydrologic conditions, gas discharge, and other geophysical conditions in the Long Valley Caldera region.</p>


<h2>San Francisco Bay Area</h2>

<p>The San Francisco Bay area has been identified in a <a href="https://pubs.usgs.gov/fs/2015/3009/">recent report</a> as a region where a magnitude 6.7 or greater earthquake is likely to occur in the next 30 years. USGS installed a variety of instruments that measure strain changes and fault slip near the many active earthquake faults in the region.</p>

